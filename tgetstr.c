// char *tgetstr(const char *id, char **area);
// id: идентификатор строки, которую вы хотите извлечь.
// area: указатель на указатель, куда будет сохранён адрес области памяти для хранения строки.
// Возвращаемое значение:

// Если строка успешно найдена, то tgetstr возвращает указатель на строку в области памяти.
// Если строка не найдена, возвращается NULL.
// Обратите внимание, что второй параметр area может использоваться для хранения строки в том случае, если строка короткая. Однако если строка слишком длинная, она может храниться в другом месте, и тогда area будет равен NULL.
#include <curses.h>
#include <term.h>
#include <stdio.h>

int main() {
    setupterm(NULL, STDOUT_FILENO, NULL);

    char *clear_screen = tgetstr("clear", NULL);

    if (clear_screen != NULL) {
        putp(clear_screen);  // Вывести строку для очистки экрана
    } else {
        printf("Строка для очистки экрана не найдена.\n");
    }

    return 0;
}

